         <h1>Code of Conduct</h1>
     </div>

     <p class="lead">Wir haben uns diesen <em>Code of Conduct</em> gegeben, damit wir alle freundlich und freundschaftlich gemeinsam an der Verbreitung politischen Wissens arbeiten können.</p>

     <h2>Auf wen trifft dieser CoC zu?</h2>
     <p>Dieser Code of Conduct trifft auf alle Menschen (und natürlich auch Bots), die sich an unserem Projekt beteiligen. Dies beinhaltet alle, die Begriffe hinzufügen und definieren, und alle, die an der Quelltext-Entwicklung unseres Projekts mitarbeiten.</p>
     <h2>Sei freundlich!</h2>
     <p>Immer, wenn du etwas zu CURIA beiträgst, bemühe dich es freundlich zu formulieren. Wenn du etwas nicht sagen kannst, ohne irgendjemanden zu beleidigen oder anzugreifen, dann lass es lieber.</p>
     <p>Diese Regel sollte eine gute Grundlage bieten, dennoch sind die folgenden Sachen noch einmal explizit verboten:</p>
     <ul>
         <li>Diskriminierung jeder Art, ob wegen politischen Gründen, aus Gründen der Identität oder anderem</li>
         <li>Aufruf zu, Ankündigung von oder Glorifizierung von jeglicher Art von Gewalt, Hass oder Verbrechen</li>
         <li>Mobbing oder andere gezielte Angriffe gegen einzelne Menschen</li>
     </ul>
     <p>Auf keinen Fall sollen diese Regeln so ausgelegt werden, dass die Auslegung dem Geist dieser Regeln widerspricht. Dies gilt insbesondere für zu enge, "textnahe" Auslegungen.</p>
     <h2>Sei sachlich!</h2>
     <p>Bei CURIA bemühen wir uns um Sachlichkeit und politische Neutralität. </p>
     <p>Insbesondere ist es verboten, politisch geladene oder einseitige Stellungnahmen in den Beschreibungen zu verwenden. Soweit nicht objektiv unmöglich, müssen die Beschreibungen sachlich und politisch neutral verfasst werden, ohne jegliche wertende Komponente.</p>
     <h2>Durchsetzung</h2>
     <p>Wer gegen diesen Code of Conduct verstößt, kann zeitweise oder dauerhaft von der Nutzung der Plattform ausgeschlossen werden.</p>
     <p>Wenn du einen Begriff siehst, der gegen diesen CoC verstößt, kannst du ihn <em>melden</em>. Dann wird sich unser Team um die Entfernung des Begriffs kümmern.</p>
